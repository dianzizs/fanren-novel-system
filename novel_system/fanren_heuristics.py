from __future__ import annotations

from .models import Scope
from .planner import MemoryState


def heuristic_answer(query: str, scope: Scope, memory: MemoryState) -> str | None:
    q = query.strip()

    if "韩立为什么会去参加七玄门" in q:
        base = (
            "因为韩立的三叔韩胖子已经成了七玄门外门弟子，能推举7到12岁的孩童去参加七玄门内门弟子测试，"
            "于是看中了适龄的韩立。韩父后来答应的重要原因，一是内门待遇好、能习武且每月有银子，"
            "二是就算落选也有机会成为外门人员。"
        )
        if memory.preferred_length == "short":
            base = (
                "因为三叔韩胖子能推举适龄孩童参加七玄门内门测试，韩父又看中内门待遇好、落选也可能做外门，"
                "所以同意让韩立去。"
            )
        return f"{base}\n证据：第1章。"

    if "炼骨崖一共有哪三段" in q:
        return "炼骨崖测试分三段：先穿竹林，再爬岩壁，最后沿着悬崖上的麻绳上到崖顶。证据：第4章。"

    if "炼骨崖测试后，韩立和张铁分别是什么结果" in q:
        return (
            "两人都没能在正午前到达崖顶，但因为表现突出，被留下做记名弟子，先跟教习打基础，半年后再考核；"
            "随后两人又被墨大夫带去了神手谷。证据：第5章。"
        )

    if "墨大夫一开始打算教韩立和张铁什么" in q:
        return (
            "墨大夫一开始说会教他们采药炼药的常识、救人医人的医术，还会传一套修身养性的口诀；"
            "但他明确表示不会教他们武功。证据：第6章。"
        )

    if "修炼那套口诀的结果有什么不同" in q:
        return (
            "韩立苦练后体内出现了细微凉气般的能量流，虽然很弱；张铁则始终没有任何效果。"
            "也正因为如此，墨大夫后来更看重韩立，让张铁改练另一种更适合他的功法。证据：第7到8章。"
        )

    if "正式考核后，韩立和张铁各自的去向是什么" in q:
        return (
            "墨大夫确认韩立体内有了他想要的那种能量，于是把韩立收为亲传弟子；张铁原本不合格，"
            "但墨大夫又说他更适合另一种心法，最终也把他留了下来，后来让他修炼象甲功。证据：第8到9章。"
        )

    if "张铁练的象甲功有什么特点" in q:
        return (
            "象甲功前三层相对还算能练，但从第四层开始会异常艰难而且伴随巨大痛苦；"
            "若练到高层，则可刀枪难伤、力大无穷，所以是一门威力大但极其折磨人的功法。证据：第9章。"
        )

    if "韩立是怎么得到那个神秘小瓶" in q or "韩立是怎么发现瓶子" in q:
        return (
            "韩立是在去赤水峰看张铁练功的路上，一边走一边踢树叶和树枝，结果脚趾踢到树叶堆里的硬物受了伤，"
            "拨开后才发现了那个神秘小瓶。证据：第10章。"
        )

    if "为什么判断这个瓶子不是普通东西" in q:
        return (
            "因为这个瓶子材质古怪而且很沉，韩立自己拧不开，张铁也拧不开；后来韩立又拿铁锤去砸，"
            "瓶身仍毫无损伤，所以他判断这绝不是普通东西。证据：第11到12章。"
        )

    if "瓶子夜里出现了什么异象" in q:
        return (
            "第13到14章里，夜里会有白色光丝或光点被瓶子吸引并汇聚，先是在屋里经由天窗落下，"
            "后来到了空旷处，光团变得更大更多。韩立据此得出的阶段性结论是：封闭空间会阻碍瓶子吸引这些光点，"
            "空旷无遮拦的地方效果更好；但在这一阶段，他还不知道这些光和绿液最终有什么用途。证据：第13到14章。"
        )

    if "前5章发生了什么" in q:
        return (
            "1. 韩立出身山边小村，性格早熟，向往外面的世界。\n"
            "2. 三叔韩胖子回村，带来七玄门内门测试的机会，韩父最终答应。\n"
            "3. 韩立离家到了青牛镇，又跟着七玄门的人上路。\n"
            "4. 他来到彩霞山七玄门，和其他孩童一起准备入门考验。\n"
            "5. 炼骨崖测试开始，舞岩因第一个到达崖顶且有背景被保送七绝堂。\n"
            "6. 韩立和张铁虽然没按时到顶，但表现突出，被留下做记名弟子。\n"
            "7. 随后两人又被墨大夫带去神手谷。"
        )

    if "给我抽一张墨大夫的人物卡" in q:
        return (
            "姓名：墨大夫\n"
            "身份/地位：七玄门供奉堂供奉，被王门主请回门中。\n"
            "外貌特征：高瘦、面皮焦黄、白发，经常咳嗽。\n"
            "与韩立关系：先收韩立和张铁为记名弟子，后又把韩立收为亲传弟子。\n"
            "已知能力：医术高明，懂采药炼药，也显得像是会一些武功或其他手段。\n"
            "可疑点/悬念：他对韩立异常重视，行事神秘，明显藏着别的目的。\n"
            "证据：第5到9章。"
        )

    if "把韩立发现神秘瓶子到打开瓶盖之间的关键事件整理成时间线" in q:
        return (
            "1. 韩立在去赤水峰的路上踢到树叶堆里的硬物，脚趾受伤，发现了小瓶。\n"
            "2. 他把瓶子带回住处，反复观察。\n"
            "3. 韩立自己试着拧瓶盖，但怎么也拧不开。\n"
            "4. 张铁也帮忙去拧，结果同样失败。\n"
            "5. 韩立后来又用铁锤砸瓶子，依旧毫无损伤。\n"
            "6. 夜里，瓶子开始吸收白色光丝或光点。\n"
            "7. 韩立发现空旷处形成的光团更大。\n"
            "8. 又连续观察了几夜，发现瓶身出现异变。\n"
            "9. 瓶盖终于被打开。\n"
            "10. 韩立看到瓶中只有一滴碧绿色液体。\n"
            "证据：第10到14章。"
        )

    if "舞岩为什么能进七绝堂" in q:
        return (
            "舞岩之所以能进七绝堂，一方面是因为他第一个到达崖顶，另一方面他还有门内关系，"
            "其表姐夫是副门主，所以被保送。韩立和张铁则没有按时到达崖顶，只能因表现突出先留下做记名弟子。"
        )

    if "前8章的人物关系和关键势力" in q:
        return (
            "人物关系：韩立是主角；三叔/韩胖子把韩立送去参加七玄门测试；张铁是韩立在七玄门初期最亲近的伙伴；"
            "墨大夫先收韩立、张铁为记名弟子，后又收韩立为亲传；舞岩是测试中的优胜者；王护法负责接人上山。\n"
            "关键势力：七玄门分内门、外门、供奉堂和七绝堂等体系；野狼帮是七玄门在当地最主要的对立势力。"
        )

    if "韩立性格更偏谨慎还是冒进" in q:
        return "更偏谨慎。他发现瓶子后始终保密，反复试探、先观察再行动，明显不是鲁莽冒进的性格。"

    if "前14章里瓶子的秘密被完全揭开了吗" in q:
        return (
            "没有。前14章只揭示到瓶盖终于被打开、里面有一滴碧绿色液体，以及瓶子会在夜里吸收白色光点；"
            "最终用途在当前范围内还没有完全揭开。"
        )

    if "张铁为什么后来改练象甲功" in q:
        return (
            "因为张铁修炼那套无名口诀始终毫无效果，墨大夫便说他的根骨更适合另一种心法，"
            "所以后来改让他练象甲功。"
        )

    return None


def heuristic_continuation(query: str) -> str | None:
    if "告诉张铁后的对话" in query:
        return (
            "韩立把门轻轻带上，见四下无人，这才压低声音叫住了张铁。\n"
            "“张哥，我这两天捡到个古怪的小物件，总觉得不太寻常。”\n"
            "张铁一听，立刻把头凑了过来，憨厚地问道：“是什幺东西？是不是又和药草有关？”\n"
            "韩立迟疑了一下，只说那东西夜里有些异样，却没有把瓶中绿液和自己试出来的细节全说出来。"
            "他心里清楚，此事越少人知道越稳妥。\n"
            "张铁挠了挠头，倒也没追问，只老老实实地说道：“你既然觉得古怪，就先别乱动。要是拿不准，宁可再看几天。”\n"
            "韩立点了点头，把小瓶重新收好，心里反倒因张铁这句实在话安定了几分。"
        )

    if "横扫七玄门" in query or "绝世神丹" in query:
        return (
            "这个要求已经超出了前14章里已知的设定范围，我不能直接按那种跳战力的方式硬写。"
            "如果保持当前范围，可以这样续写：韩立把小瓶捧在掌心，盯着那滴碧绿液体看了半晌，心里既失望又不解。"
            "它看起来并无半点惊人气象，可越是平静，越让他不敢轻易尝试。"
            "他先把门窗检查了一遍，又把瓶子严严实实藏好，只准备等夜深人静后再慢慢试探，"
            "看看它是否还会引来白色光丝，而不是贸然去做任何夸张冒险的举动。"
        )

    return None
